-- keep updated_at fresh
create or replace function touch_updated_at() returns trigger as $$
begin new.updated_at = now(); return new; end;
$$ language plpgsql;

drop trigger if exists artworks_touch on artworks;
create trigger artworks_touch before update on artworks
  for each row execute function touch_updated_at();

-- ============================================================
-- Storage bucket for images. Run once.
-- ============================================================
insert into storage.buckets (id, name, public)
values ('artwork-images', 'artwork-images', true)
on conflict (id) do nothing;

-- ============================================================
-- Row Level Security
-- Private to signed-in users. Since this is a single-user archive (you),
-- "any authenticated user" is enough to keep it private once Auth is on.
-- To make it multi-user later, add a user_id column and scope to auth.uid().
-- ============================================================
alter table artworks enable row level security;
alter table collections enable row level security;
alter table relationships enable row level security;

drop policy if exists artworks_all on artworks;
create policy artworks_all on artworks for all
  using (auth.role() = 'authenticated') with check (auth.role() = 'authenticated');

drop policy if exists collections_all on collections;
create policy collections_all on collections for all
  using (auth.role() = 'authenticated') with check (auth.role() = 'authenticated');

drop policy if exists relationships_all on relationships;
create policy relationships_all on relationships for all
  using (auth.role() = 'authenticated') with check (auth.role() = 'authenticated');

-- storage policies — readable + writable by signed-in users
drop policy if exists "public read" on storage.objects;
create policy "auth read" on storage.objects
  for select using (bucket_id = 'artwork-images' and auth.role() = 'authenticated');

drop policy if exists "anon write" on storage.objects;
create policy "auth write" on storage.objects
  for insert with check (bucket_id = 'artwork-images' and auth.role() = 'authenticated');

drop policy if exists "anon update" on storage.objects;
create policy "auth update" on storage.objects
  for update using (bucket_id = 'artwork-images' and auth.role() = 'authenticated');

drop policy if exists "anon delete" on storage.objects;
create policy "auth delete" on storage.objects
  for delete using (bucket_id = 'artwork-images' and auth.role() = 'authenticated');
